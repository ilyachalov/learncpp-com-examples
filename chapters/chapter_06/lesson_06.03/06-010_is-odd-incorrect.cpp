// is-odd-incorrect.cpp
// демонстрация работы функции, определяющей нечетность заданного числа

#include <iostream>

bool isOdd(int x)
{
    return (x % 2) == 1;
}

int main()
{
    std::cout << isOdd(5) << '\n';  // 1 (правильно)
    std::cout << isOdd(-5) << '\n'; // 0 (неправильно, должно быть 1)

    // функция isOdd работает неправильно для отрицательных нечетных чисел, так
    // как остаток от целочисленного деления в C++ всегда принимает знак
    // делимого, то есть (-5 % 2) даст результат -1, который не совпадает с 1

    return 0;
}